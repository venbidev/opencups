    for row in results:
        response_text += (
            f"–û–ª–∏–º–ø–∏–∞–¥–∞: {row['name']} ({row['date']})\n"
            f"–ü—Ä–µ–¥–º–µ—Ç: {row['subject'] if row['subject'] else '-'}\n"
            f"–§–ò–û: {row['full_name']}\n"
            f"–ë–∞–ª–ª—ã: {row['score'] if row['score'] is not None else '-'}\n"
            f"–ú–µ—Å—Ç–æ: {row['place'] if row['place'] is not None else '-'}\n"
            f"–î–∏–ø–ª–æ–º: {row['diploma_link'] if row['diploma_link'] else '–ù–µ—Ç'}\n"
            f"--------------------\n"
        )
    await update.message.reply_text(response_text)

